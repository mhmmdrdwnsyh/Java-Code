public class luasLingkaran 
{
		public static void main(String[] args)
		{
		final double PHI = 3.14159265358979323;
		double p;
		int r;
		
		r = 5;
		p = PHI * r * r;
		
		System.out.println("Jari-jari lingkaran adalah = " + r);
		System.out.println("Luas lingkaran adalah = " + p);

		
		}
}
