public class operasiFloatingpoint {
    public static void main(String args[] ){
        double a, b;
        a = 4.25;
        b = 1.5;
        System.out.println("a * b = " + (a * b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a + b = " + (a + b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
